#Tuples in python

tup = ( 2, 4, 1, 3)
print(type(tup))
print(tup[0])
print(tup[2])


tup =()
print(tup)
print(type(tup))

tup = (1,)
print(tup)
print(type(tup))

tup = (1)
print(tup)
print(type(tup))


tup = ("Hello",)
print(tup)
print(type(tup))


tup = ("Hello")
print(tup)
print(type(tup))


tup = ( 2, 4, 1, 3)
print(tup[1:3])



print("\n")
tup = ( 1,2,3,1)
print(tup.index(2))
print(tup.count(1))
